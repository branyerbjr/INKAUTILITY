package Models.Hardware;

public class OS {
    private int idOS;
    private String systemOs;

    public OS() {
    }

    public int getIdOS() {
        return idOS;
    }

    public void setIdOS(int idOS) {
        this.idOS = idOS;
    }

    public String getSystemOs() {
        return systemOs;
    }

    public void setSystemOs(String systemOs) {
        this.systemOs = systemOs;
    }
}
